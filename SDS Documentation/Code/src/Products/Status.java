/**
 * The Status enumeration represents the availability of a product in the inventory.
 * This enum has two possible values: "OutOfStock" and "Available".
 */

package Products;
public enum Status {
    /**
     * Represents the status of a product when it is out of stock.
     */
    OutOfStock,

    /**
     * Represents the status of a product when it is available in the inventory.
     */
    Available ;
}
